// Initialize GSAP and ScrollTrigger
gsap.registerPlugin(ScrollTrigger);

// Function to translate the page
function translatePage(lang) {
    // Update all elements with data-lang-key attributes
    for (const key in translations[lang]) {
        const elements = document.querySelectorAll(`[data-lang-key="${key}"]`);
        elements.forEach(el => {
            if (el.tagName === 'INPUT') {
                el.value = translations[lang][key];
            } else {
                el.innerHTML = translations[lang][key];
            }
        });
    }
    
    // Update HTML lang attribute
    document.documentElement.lang = lang;
}

// Language switcher functionality
document.addEventListener('DOMContentLoaded', function() {
    const langButtons = document.querySelectorAll('.lang-button');
    
    // Set click handlers for language buttons
    langButtons.forEach(button => {
        button.addEventListener('click', function() {
            // Update active button
            langButtons.forEach(btn => btn.classList.remove('active'));
            this.classList.add('active');
            
            // Translate page
            const lang = this.getAttribute('data-lang');
            translatePage(lang);
            
            // Save preference
            localStorage.setItem('preferredLang', lang);
        });
    });
    
    // Check for saved language preference
    const savedLang = localStorage.getItem('preferredLang') || 'en';
    const activeButton = document.querySelector(`.lang-button[data-lang="${savedLang}"]`);
    if (activeButton) {
        activeButton.click(); // This will trigger translation
    } else {
        translatePage('en'); // Default to English
    }

    // Set up animations
    setupAnimations();
});

// Animation setup
function setupAnimations() {
    // Animate sections on scroll
    gsap.utils.toArray(".section").forEach((section, i) => {
        gsap.from(section, {
            scrollTrigger: {
                trigger: section,
                start: "top 75%",
                toggleActions: "play none none none",
                once: true
            },
            opacity: 0,
            y: 50,
            duration: 0.8,
            ease: "power2.out",
            delay: i * 0.1
        });
    });

    // Header animation
    gsap.from(".header", {
        duration: 1,
        y: -30,
        opacity: 0,
        ease: "power2.out"
    });

    // Footer animation
    gsap.from(".footer", {
        scrollTrigger: {
            trigger: ".footer",
            start: "top 95%",
            toggleActions: "play none none none"
        },
        opacity: 0,
        y: 30,
        duration: 0.8,
        ease: "power2.out"
    });
}